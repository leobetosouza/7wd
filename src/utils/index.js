export const shuffle = array => {
    const a = [...array];
    for (let i = a.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [a[i], a[j]] = [a[j], a[i]];
    }
    return a;
};

export const delay = async ms =>
    new Promise(resolve => setTimeout(() => resolve(), ms));